    private void telponCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_telponCaretUpdate
        // TODO add your handling code here:
        
        if (telpon.getText().length()==0 && !telpon.getText().matches("[A-Z]")&& !telpon.getText().matches("[a-z]")) {
            proces.setEnabled(false);

        }
        else if (telpon.getText().length()!=0 && telpon.getText().matches("[1-9]*") ) {
            proces.setEnabled(true);
        }
    }//GEN-LAST:event_telponCaretUpdate

    private void telponKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_telponKeyPressed
        // TODO add your handling code here:
        telpon.addKeyListener(new KeyAdapter() 
        {
            public void keyTyped(KeyEvent e)
            {
            char karakter = e.getKeyChar();
                if ((((karakter<'0' || karakter > '9'))&& (karakter != '\b'))&&(karakter !='.')) {
                e.consume();
                }
            }
});
        
    }//GEN-LAST:event_telponKeyPressed

