    private void procesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_procesActionPerformed
        // TODO add your handling code here:
        double barang=0,express,jenis = 0,totaldiskon,regulerr=11500,diskon,totaldisc;
        diskon=discon.getValue();
        Name.getText();
        telpon.getText();
        barang= Integer.parseInt(berat.getText());
        double regu = regulerr*barang*(diskon/100);
        double eks = regulerr*1.5*barang*(diskon/100);
        if (reguler.isSelected())
        {
            jenis = (regulerr*barang)-regu;
        }
        else 
            if(expres.isSelected())
        {
            jenis = (regulerr*barang*1.5)-eks; 
        }
        hargatotal.setText(String.valueOf(jenis));
        
       
    }//GEN-LAST:event_procesActionPerformed

